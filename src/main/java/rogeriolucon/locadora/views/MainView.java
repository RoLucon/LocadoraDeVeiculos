    private LocalDate stringDateToLocalDate(String date) {
        try{
            return LocalDate.parse(date, DateTimeFormatter.ofPattern(MY_DATA_F));
        } catch(Exception e){
            return null;
        }
    }
    
    private String dateStringNow(){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern(MY_DATA_F);
        return dtf.format(LocalDate.now()).toString();
    }
    
    private String dateToString(LocalDate date){
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern(MY_DATA_F);
        return dtf.format(date).toString();
    }
    
    private int difBetweenDates(LocalDate start, LocalDate end){
        return (int)ChronoUnit.DAYS.between(start, end);
    }
    
    private double rentPrice(String start, String end){
        long daysBetween = ChronoUnit.DAYS.between(stringDateToLocalDate(start), stringDateToLocalDate(end));
        double value = daysBetween * mainService.rentValuePerDay();
        return value == 0 ? mainService.rentValuePerDay() : value;
    }
    
    private double rentPrice(LocalDate start, LocalDate end){
        long daysBetween = ChronoUnit.DAYS.between(end, start);
        double value = daysBetween * mainService.rentValuePerDay();
        return value == 0 ? mainService.rentValuePerDay() : value;
    }
    
